-module(gate_way_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    lager:start(),
    ok = application:start(ranch),
    {ok, _} = ranch:start_listener(gate_way, 100,
        ranch_tcp, [{port, 5555}],
        gate_protocol, []
        ).
%%     gate_way_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

